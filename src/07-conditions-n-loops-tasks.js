/**
 * Returns the common directory path for specified array of full filenames.
 *
 * @param {array} pathes
 * @return {string}
 *
 * @example:
 *   ['/web/images/image1.png', '/web/images/image2.png']  => '/web/images/'
 *   ['/web/assets/style.css', '/web/scripts/app.js',  'home/setting.conf'] => ''
 *   ['/web/assets/style.css', '/.bin/mocha',  '/read.me'] => '/'
 *   ['/web/favicon.ico', '/web-scripts/dump', '/verbalizer/logs'] => '/'
 */
function getCommonDirectoryPath(pathes) {
  function parseCommonPath(path) {
    const arr = path.split('/');
    let result = arr.slice(0, -1).join('/');
    if (path.indexOf('/') >= 0) result += '/';
    return result;
  }

  const len = Math.min(...pathes.map((x) => x.length));
  let commonPath = '';
  for (let i = 0; i < len; i += 1) {
    const symbol = pathes[0][i];
    const isAllTheSame = pathes.reduce((acc, path) => acc * (path[i] === symbol), 1);
    if (isAllTheSame) commonPath += symbol;
    else return parseCommonPath(commonPath);
  }
  return parseCommonPath(commonPath);
}


/**
 * Returns the product of two specified matrixes.
 * See details: https://en.wikipedia.org/wiki/Matrix_multiplication
 *
 * @param {array} m1
 * @param {array} m2
 * @return {array}
 *
 * @example:
 *   [[ 1, 0, 0 ],       [[ 1, 2, 3 ],           [[ 1, 2, 3 ],
 *    [ 0, 1, 0 ],   X    [ 4, 5, 6 ],     =>     [ 4, 5, 6 ],
 *    [ 0, 0, 1 ]]        [ 7, 8, 9 ]]            [ 7, 8, 9 ]]
 *
 *                        [[ 4 ],
 *   [[ 1, 2, 3]]    X     [ 5 ],          =>     [[ 32 ]]
 *                         [ 6 ]]
 *
 */
function getMatrixProduct(m1, m2) {
  const product = [];
  for (let i = 0; i < m1.length; i += 1) {
    const line = [];
    for (let j = 0; j < m2[0].length; j += 1) {
      let currentNumber = 0;
      for (let k = 0; k < m1[0].length; k += 1) {
        currentNumber += m1[i][k] * m2[k][j];
      }
      line.push(currentNumber);
    }
    product.push(line);
  }
  return product;
}


/**
 * Returns the evaluation of the specified tic-tac-toe position.
 * See the details: https://en.wikipedia.org/wiki/Tic-tac-toe
 *
 * Position is provides as 3x3 array with the following values: 'X','0', undefined
 * Function should return who is winner in the current position according to the game rules.
 * The result can be: 'X','0',undefined
 *
 * @param {array} position
 * @return {string}
 *
 * @example
 *
 *   [[ 'X',   ,'0' ],
 *    [    ,'X','0' ],       =>  'X'
 *    [    ,   ,'X' ]]
 *
 *   [[ '0','0','0' ],
 *    [    ,'X',    ],       =>  '0'
 *    [ 'X',   ,'X' ]]
 *
 *   [[ '0','X','0' ],
 *    [    ,'X',    ],       =>  undefined
 *    [ 'X','0','X' ]]
 *
 *   [[    ,   ,    ],
 *    [    ,   ,    ],       =>  undefined
 *    [    ,   ,    ]]
 *
 */
function evaluateTicTacToePosition(position) {
  let isXWinner = false;
  let is0Winner = false;
  function setFlag(item, bool) {
    if (bool) {
      if (item === 'X') isXWinner = bool;
      if (item === '0') is0Winner = bool;
    }
  }

  function detectItemInMatrix(number) {
    const i = number % 3;
    const j = Math.floor(number / 3);
    return position[i][j];
  }

  const combinations = [[0, 1, 2], [3, 4, 5], [6, 7, 8], [0, 3, 6],
    [1, 4, 7], [2, 5, 8], [0, 4, 8], [2, 4, 6]];

  combinations.forEach((winLine) => {
    const arr = winLine.map((x) => detectItemInMatrix(x));
    const symbol = arr[0];
    const isTheSame = arr[0] === arr[1] && arr[1] === arr[2];
    setFlag(symbol, isTheSame);
  });

  if (is0Winner === isXWinner) return undefined;
  return isXWinner ? 'X' : '0';
}


